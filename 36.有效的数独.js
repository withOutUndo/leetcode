/**
 * @param {character[][]} board
 * @return {boolean}
 */
var isValidSudoku = function (board) {
  let col = new Array(9).fill(0),
    row = new Array(9).fill(0),
    area = new Array(9).fill(0);

  for (let i = 0; i < 9; i++) {
    for (let j = 0; j < 9; j++) {
      const c = board[i][j];

      if (c === ".") {
        continue;
      }

      let m = (i / 3) | 0;
      let n = (j / 3) | 0;
      const o = m * 3 + n;
      if (
        ((col[i] >> c) & 1) == 1 ||
        ((row[j] >> c) & 1) == 1 ||
        ((area[o] >> c) & 1) == 1
      )
        return false;

      col[i] |= 1 << c;
      row[j] |= 1 << c;
      area[o] |= 1 << c;
    }
  }

  return true;
};

